package poly.noinheri;

public class Ghost {
	private int hp;
	private int atk;
	private int def;

	public Ghost() {
		this.hp = 20;
		this.atk = 10;
		this.def = 1;
	}

	public int getHp() {
		return hp;
	}

	public void setHp(int hp) {
		this.hp = hp;
	}

	public int getAtk() {
		return atk;
	}

	public void setAtk(int atk) {
		this.atk = atk;
	}

	public int getDef() {
		return def;
	}

	public void setDef(int def) {
		this.def = def;
	}
	
	
}
